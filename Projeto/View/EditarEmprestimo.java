    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        try {
            int id = 0;
            String dataInicio = "";
            String dataFinal = "";
            String devolucao = "";
            Amigos amigo = new Amigos();
            Ferramentas ferramenta = new Ferramentas();

            if (this.cb_Amigo.getSelectedItem() == "-") {
                throw new Mensagens("Por favor, selecione o campo Amigo.");
            } else {
                amigo = (Amigos) this.cb_Amigo.getSelectedItem();
            }

            if (this.cb_Ferramenta.getSelectedItem() == "-") {
                throw new Mensagens("Por favor, selecione o campo Ferramenta.");
            } else {
                ferramenta = (Ferramentas) this.cb_Ferramenta.getSelectedItem();
            }

            if (this.dataInicial.getText().isEmpty()) {
                throw new Mensagens("Por favor, preencha o campo Data Inicial.");
            } else if (this.dataInicial.getText().trim().length() < 0 && this.dataInicial.getText().trim().length() > 10) {
                throw new Mensagens("Data Inicial precisa ser maior que 0 caracteres e menor ou igual a 10.");
            } else {
                dataInicio = this.dataInicial.getText();
            }

            if (this.dataFinal.getText().isEmpty()) {
                throw new Mensagens("Por favor, preencha o campo Prazo.");
            } else if (this.dataFinal.getText().length() != 10) {
                throw new Mensagens("Por favor, preencha o campo Prazo corretamente ( YYYY / MM / DD ).");
            } else {
                dataFinal = this.dataFinal.getText();
            }
            if (this.jTableEmprestimo.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro selecione um Empréstimo para Alterar");
            } else {
                id = Integer.parseInt(this.jTableEmprestimo.getValueAt(this.jTableEmprestimo.getSelectedRow(), 0).toString());
            }

            if (this.objetoEmprestimo.UpdateEmprestimoDB(id, ferramenta, amigo, dataInicio, dataFinal)) {
                this.objetoEmprestimoTotal.UpdateEmprestimoTotalDB(id, ferramenta, amigo, dataInicio, dataFinal, devolucao);

                this.cb_Amigo.setSelectedItem("");
                this.cb_Ferramenta.setSelectedItem("");
                this.dataInicial.setText("");
                this.dataFinal.setText("");

                JOptionPane.showMessageDialog(null, "Empréstimo Alterado com Sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
            }

            System.out.println(this.objetoEmprestimo.getListaEmprestimos().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        } catch (NumberFormatException erro2) {
            JOptionPane.showMessageDialog(null, "Informe um número. ", "ERRO", JOptionPane.ERROR_MESSAGE);
        } finally {
            carregaTabela();
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

