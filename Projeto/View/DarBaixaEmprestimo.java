    private void btn_DarBaixaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DarBaixaActionPerformed
        try {
            int id = 0;
            
            if (this.jTableEmprestimos.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro Selecione um Empréstimo para Dar Baixa");
            } else {
                id = Integer.parseInt(this.jTableEmprestimos.getValueAt(this.jTableEmprestimos.getSelectedRow(), 0).toString());
            }

            int resposta_usuario = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Dar Baixa neste Empréstimo?");

            if (resposta_usuario == 0) {
                if (this.objetoEmprestimos.DeleteEmprestimoDB(id)) {
                    
                    JOptionPane.showMessageDialog(null, "Baixa Dada com Sucesso.", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                }
            }
            System.out.println(this.objetoEmprestimos.getListaEmprestimos().toString());

        } catch (Mensagens erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }finally{
            carregaTabela();
        }
    }//GEN-LAST:event_btn_DarBaixaActionPerformed

