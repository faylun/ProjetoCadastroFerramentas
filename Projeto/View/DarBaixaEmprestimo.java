    @SuppressWarnings("unchecked")
    public void carregaTabela() {
        DefaultTableModel modelo = (DefaultTableModel) this.jTableEmprestimos.getModel();
        modelo.setNumRows(0);

        ArrayList<Emprestimos> listaEmprestimos = new ArrayList<>();
        listaEmprestimos = objetoEmprestimos.getListaEmprestimos();

        for (Emprestimos e : listaEmprestimos) {
            modelo.addRow(new Object[]{
                e.getId(),
                e.getAmigo().getNome(),
                e.getFerramenta().getNome(),
                e.getDataEmprestimo(),
                e.getDataDevolucao()
            });
        }
    }

