    @SuppressWarnings("unchecked")
    public void carregaTable() {
        DefaultTableModel modelo = (DefaultTableModel) this.jTableEmprestimoTotal.getModel();
        modelo.setNumRows(0);

        ArrayList<EmprestimoTotal> listaEmprestimoTotal = new ArrayList<>();
        listaEmprestimoTotal = objetoEmprestimoTotal.getListaEmprestimoTotal();

        for (EmprestimoTotal e : listaEmprestimoTotal) {
            modelo.addRow(new Object[]{
                e.getId(),
                e.getAmigo().getNome(),
                e.getFerramenta().getNome(),
                e.getDataEmprestimo(),
                e.getDataDevolucao(),
                e.getDevolucao()
            });
        }
    }

