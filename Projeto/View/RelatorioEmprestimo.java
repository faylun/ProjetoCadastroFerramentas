    private void btn_salvar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvar1ActionPerformed
        try {
            int id = 0;
            String devolucao = "";

            if (this.devolucao.getText().isEmpty()) {
                throw new Mensagens("Por favor, preencha o campo Devolução.");
            } else if (this.devolucao.getText().length() != 10) {
                throw new Mensagens("Por favor, preencha o campo Devolução corretamente ( YYYY / MM / DD ) ");
            } else {
                devolucao = this.devolucao.getText();
            }

            if (this.jTableEmprestimoTotal.getSelectedRow() == -1) {
                throw new Mensagens("Primeiro selecione um Empréstimo para Alterar a Data de Devolução.");
            } else {
                id = Integer.parseInt(this.jTableEmprestimoTotal.getValueAt(this.jTableEmprestimoTotal.getSelectedRow(), 0).toString());
            }

            if (this.objetoEmprestimoTotal.UpdateEmprestimoTotalDDB(id, devolucao)) {
                JOptionPane.showMessageDialog(null, "Devolução Cadastrada com Sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
                
                this.devolucao.setText("");

                this.dispose();
                new RelatorioEmprestimo().setVisible(true);
            }
            System.out.println(this.objetoEmprestimoTotal.getListaEmprestimoTotal().toString());
        } catch (Mensagens | HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_salvar1ActionPerformed

